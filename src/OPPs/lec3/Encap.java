package OPPs.lec3;

public class Encap {
    /*

        Encapsulation -> Wrapping up the implementation of the data members and methods in a class

        Abstraction -> Hiding unnecessary details and showing valuable information

        Difference btw them is :-
        Abstraction solves design level issue
        Encapsulation solves implementation level issue

        e.g. there is a function ArrayList, how classes and objects are made under ArrayList is part of encapsulation
             and how ArrayList is used without knowing internal implementation is under Abstraction

        Data Hiding -> Concerns about security of the system
        Encapsulation reduces the designable complexity of code, but we can access the code
        While in data Hiding we cannot access the code.

     */
}
